//WAP to display marks, age, section of a student
class VariableDemo{
    public static void main(String[] args){
        // declare variable
        // float or double
        double marks;
        int age;
        char section;
        // initialize
        // identifier = value or literal;
        marks = 40.5;
        age = 40;
        section = 'C';
        //declare and initialize an String variable
        String name = "Vaibhav Diwan";

        // printing variables values of console/output
        System.out.println("Marks = " + marks );
        System.out.println();
        System.out.println("Age = " + age); 
        System.out.println();
        System.out.println("Section = " + section);
        System.out.println();
        System.out.println(name);
    }
}


